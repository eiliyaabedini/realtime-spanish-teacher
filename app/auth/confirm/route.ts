import { NextResponse } from "next/server";
import type { EmailOtpType } from "@supabase/supabase-js";
import { createClient } from "@/lib/supabase/server";

/**
 * Verifies email OTP links carrying a token_hash (admin-generated links and
 * custom email templates). Browser-initiated PKCE links (?code=) are handled
 * by /auth/callback instead.
 */
export async function GET(request: Request) {
  const { searchParams, origin } = new URL(request.url);
  const tokenHash = searchParams.get("token_hash");
  const type = searchParams.get("type") as EmailOtpType | null;
  const next = searchParams.get("next") ?? "/lessons";

  if (tokenHash && type) {
    const supabase = await createClient();
    const { error } = await supabase.auth.verifyOtp({ type, token_hash: tokenHash });
    if (!error) {
      return NextResponse.redirect(`${origin}${next.startsWith("/") ? next : "/lessons"}`);
    }
  }

  return NextResponse.redirect(`${origin}/login?error=auth`);
}
